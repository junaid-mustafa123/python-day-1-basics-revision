#==================================
# 1. Print Statement & Quotes
#==================================
#-------------------------------------------
# Print Hello World using single quotes.
#-------------------------------------------
print('HELLO WORLD')
#-------------------------------------------
# Print Hello World using double quotes.
print("HELLO WORLD")
#-------------------------------------------
# Print the following using triple quotes:
print("""HELLO WORLD""")
#-------------------------------------------
# Print the following using triple quotes:
# Hello
# Python
# World
#-------------------------------------------
print("""
      Hello
      Python
      World
""")
#-----------------------------------------------------------------------------
# Print your name and age in a single print statement separated by a comma.
print("My name is Junaid","I am 20 year old",sep=",")
#-----------------------------------------------------------------------------
#  Print Hello and World in two lines using a single print statement.
print("HELLO\nWORLD")
#-----------------------------------------------------------------------------
# Print Python and Programming separated by a dash (-).
print('Python','Programming',sep="-")
#---------------------------------------
# Print He said "Hi" using escape sequences.
print("He Said \"Hi\"")
#--------------------------------------------
# Print the path C:\Users\Junaid\Documents using escape sequences.
print("C:\\Users\\Junaid\\Documents")


